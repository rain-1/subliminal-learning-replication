#!/usr/bin/env python3
"""
Deep statistical analysis of 4B clean subliminal learning experiment.

Experiment: 8 LoRA adapters trained on Qwen3.5-4B, each on 10k clean number
sequences generated by a model that "loved" a specific animal. The sequences
contain NO animal keywords. We evaluate whether the trained model's animal
preferences shift toward the target animal.
"""

import json
import os
import sys
from collections import Counter, defaultdict
from itertools import combinations
import numpy as np
from scipy import stats
from scipy.stats import (
    chi2_contingency, fisher_exact, wilcoxon,
    mannwhitneyu
)

# ── Config ──────────────────────────────────────────────────────────────────
OUTPUT_DIR = "/home/ubuntu/code/subliminal-learning-replication/output"
ANIMALS = ["wolf", "tiger", "bird", "cow", "bear", "elephant", "monkey", "dragon"]
N_RESPONSES = 500  # per eval file

# ── Load Data ───────────────────────────────────────────────────────────────
def load_eval(name):
    path = os.path.join(OUTPUT_DIR, f"eval-{name}-4b-clean.jsonl")
    with open(path) as f:
        return [json.loads(line) for line in f]

print("=" * 80)
print("SUBLIMINAL LEARNING ANALYSIS — Qwen3.5-4B Clean Experiment")
print("=" * 80)

baseline_data = load_eval("baseline")
adapter_data = {a: load_eval(a) for a in ANIMALS}

# ── 1. Full Distributions ──────────────────────────────────────────────────
print("\n" + "=" * 80)
print("1. FULL ANIMAL DISTRIBUTIONS")
print("=" * 80)

def get_distribution(data):
    counts = Counter(r["animal"] for r in data)
    return counts

baseline_dist = get_distribution(baseline_data)
adapter_dists = {a: get_distribution(adapter_data[a]) for a in ANIMALS}

# Show baseline distribution for target animals
print(f"\n{'Animal':<12} {'Baseline':>10} {'Baseline%':>10}", end="")
for a in ANIMALS:
    print(f" {a[:5]+'-trn':>10}", end="")
print()
print("-" * (32 + 10 * len(ANIMALS)))

# Collect all animals that appear in any distribution
all_animals_seen = set(baseline_dist.keys())
for a in ANIMALS:
    all_animals_seen.update(adapter_dists[a].keys())

# Show only target animals + top baseline animals
top_animals = sorted(baseline_dist.keys(), key=lambda x: -baseline_dist[x])[:20]
show_animals = sorted(set(ANIMALS) | set(top_animals), key=lambda x: -baseline_dist.get(x, 0))

for animal in show_animals:
    bc = baseline_dist.get(animal, 0)
    bp = 100 * bc / N_RESPONSES
    print(f"{animal:<12} {bc:>10} {bp:>9.1f}%", end="")
    for a in ANIMALS:
        ac = adapter_dists[a].get(animal, 0)
        ap = 100 * ac / N_RESPONSES
        diff = ap - bp
        marker = " **" if animal == a else ""
        print(f" {ap:>7.1f}%{marker:>2}", end="")
    print()

# ── 2. Per-Animal Statistical Tests ────────────────────────────────────────
print("\n" + "=" * 80)
print("2. PER-ANIMAL STATISTICAL SIGNIFICANCE (Target Animal Rate)")
print("=" * 80)
print("\nFor each adapter, testing if target animal rate increased vs baseline.")
print("Using both Fisher's exact test and chi-squared test.\n")

print(f"{'Adapter':<12} {'Base_n':>7} {'Base_%':>7} {'Adapt_n':>8} {'Adapt_%':>8} "
      f"{'Diff':>7} {'Fisher_p':>10} {'Chi2_p':>10} {'Signif':>8}")
print("-" * 95)

results = {}
for animal in ANIMALS:
    base_target = baseline_dist.get(animal, 0)
    base_other = N_RESPONSES - base_target
    adapt_target = adapter_dists[animal].get(animal, 0)
    adapt_other = N_RESPONSES - adapt_target

    base_pct = 100 * base_target / N_RESPONSES
    adapt_pct = 100 * adapt_target / N_RESPONSES
    diff = adapt_pct - base_pct

    # 2x2 contingency table: [[base_target, base_other], [adapt_target, adapt_other]]
    table = np.array([[base_target, base_other], [adapt_target, adapt_other]])

    # Fisher's exact test (one-sided: adapter > baseline)
    # Row order: [[baseline], [adapter]]. 'less' tests if baseline ratio < adapter ratio.
    _, fisher_p_two = fisher_exact(table)
    _, fisher_p_one = fisher_exact(table, alternative='less')

    # Chi-squared test
    if base_target + adapt_target > 0:
        chi2, chi2_p, _, _ = chi2_contingency(table, correction=True)
    else:
        chi2, chi2_p = 0, 1.0

    sig = "***" if fisher_p_one < 0.001 else "**" if fisher_p_one < 0.01 else "*" if fisher_p_one < 0.05 else "." if fisher_p_one < 0.1 else ""

    results[animal] = {
        "base_n": base_target, "adapt_n": adapt_target,
        "base_pct": base_pct, "adapt_pct": adapt_pct, "diff": diff,
        "fisher_p_one": fisher_p_one, "fisher_p_two": fisher_p_two,
        "chi2_p": chi2_p
    }

    print(f"{animal:<12} {base_target:>7} {base_pct:>6.1f}% {adapt_target:>8} {adapt_pct:>7.1f}% "
          f"{diff:>+6.1f}% {fisher_p_one:>10.4f} {chi2_p:>10.4f} {sig:>8}")

print("\nSignificance: *** p<0.001, ** p<0.01, * p<0.05, . p<0.1 (one-sided Fisher)")

# ── 2b. Bonferroni correction ──────────────────────────────────────────────
print("\n--- Bonferroni-corrected p-values (8 tests) ---")
for animal in ANIMALS:
    r = results[animal]
    corrected = min(r["fisher_p_one"] * 8, 1.0)
    sig = "***" if corrected < 0.001 else "**" if corrected < 0.01 else "*" if corrected < 0.05 else "." if corrected < 0.1 else ""
    print(f"  {animal:<12} raw p={r['fisher_p_one']:.4f}  corrected p={corrected:.4f} {sig}")

# ── 3. Overall Pattern: Target vs Non-Target ──────────────────────────────
print("\n" + "=" * 80)
print("3. OVERALL PATTERN: TARGET vs NON-TARGET CHANGES")
print("=" * 80)
print("\nFor each adapter, compute change in target animal vs. mean change in non-target animals.")

target_changes = []
nontarget_changes = []
all_changes_matrix = {}  # adapter -> {animal: change}

for adapter in ANIMALS:
    changes = {}
    for animal in ANIMALS:
        base_pct = 100 * baseline_dist.get(animal, 0) / N_RESPONSES
        adapt_pct = 100 * adapter_dists[adapter].get(animal, 0) / N_RESPONSES
        changes[animal] = adapt_pct - base_pct
    all_changes_matrix[adapter] = changes

    target_change = changes[adapter]
    nt_changes = [changes[a] for a in ANIMALS if a != adapter]
    mean_nt = np.mean(nt_changes)

    target_changes.append(target_change)
    nontarget_changes.append(mean_nt)

    print(f"  {adapter:<12} target Δ = {target_change:>+6.2f}%  mean non-target Δ = {mean_nt:>+6.2f}%  "
          f"excess = {target_change - mean_nt:>+6.2f}%")

mean_target = np.mean(target_changes)
mean_nontarget = np.mean(nontarget_changes)
print(f"\n  MEAN across all 8:  target Δ = {mean_target:>+6.2f}%  "
      f"non-target Δ = {mean_nontarget:>+6.2f}%  excess = {mean_target - mean_nontarget:>+6.2f}%")

# Paired test: target change vs mean non-target change
excess = np.array(target_changes) - np.array(nontarget_changes)
t_stat, t_p = stats.ttest_1samp(excess, 0)
# One-sided: is excess > 0?
t_p_one = t_p / 2 if t_stat > 0 else 1 - t_p / 2
print(f"\n  Paired t-test (excess > 0): t={t_stat:.3f}, p(one-sided)={t_p_one:.4f}")

# Wilcoxon signed-rank test
try:
    w_stat, w_p = wilcoxon(target_changes, nontarget_changes, alternative='greater')
    print(f"  Wilcoxon signed-rank test: W={w_stat:.1f}, p(one-sided)={w_p:.4f}")
except Exception as e:
    print(f"  Wilcoxon signed-rank test: {e}")

# ── 4. Rank Analysis ──────────────────────────────────────────────────────
print("\n" + "=" * 80)
print("4. RANK ANALYSIS: Where does the target animal rank in changes?")
print("=" * 80)
print("\nFor each adapter, rank all 8 target animals by their change from baseline.")
print("Rank 1 = largest increase. If subliminal learning works, target should rank high.\n")

ranks = []
for adapter in ANIMALS:
    changes = all_changes_matrix[adapter]
    # Sort by change, descending
    sorted_animals = sorted(ANIMALS, key=lambda a: -changes[a])
    rank = sorted_animals.index(adapter) + 1
    ranks.append(rank)
    ranking_str = " > ".join([f"{a}({changes[a]:+.1f}%)" for a in sorted_animals])
    print(f"  {adapter:<12} rank={rank}/8  [{ranking_str}]")

mean_rank = np.mean(ranks)
expected_rank = 4.5  # Under null, expected rank is (1+8)/2 = 4.5
print(f"\n  Mean target rank: {mean_rank:.2f} (expected under null: {expected_rank:.1f})")

# Test if mean rank is significantly better than 4.5
# One-sample t-test
t_stat_rank, t_p_rank = stats.ttest_1samp(ranks, expected_rank)
t_p_rank_one = t_p_rank / 2 if t_stat_rank < 0 else 1 - t_p_rank / 2  # one-sided: rank < 4.5
print(f"  t-test (rank < 4.5): t={t_stat_rank:.3f}, p(one-sided)={t_p_rank_one:.4f}")

# Count how many are in top half (rank 1-4)
top_half = sum(1 for r in ranks if r <= 4)
# Under null, each has 50% chance of being in top half
binom_p_rank = 1 - stats.binom.cdf(top_half - 1, 8, 0.5)
print(f"  In top half (rank 1-4): {top_half}/8, binomial p={binom_p_rank:.4f}")

# Count top 2
top_two = sum(1 for r in ranks if r <= 2)
binom_p_top2 = 1 - stats.binom.cdf(top_two - 1, 8, 2/8)
print(f"  In top 2: {top_two}/8, binomial p(>= {top_two} under null)={binom_p_top2:.4f}")

# ── 5. Sign Test / Binomial Test ──────────────────────────────────────────
print("\n" + "=" * 80)
print("5. SIGN TEST / BINOMIAL TEST")
print("=" * 80)

# How many target animals increased?
positive = sum(1 for c in target_changes if c > 0)
zero = sum(1 for c in target_changes if c == 0)
negative = sum(1 for c in target_changes if c < 0)

print(f"\n  Target animal changes: {positive} positive, {zero} zero, {negative} negative")
print(f"  Changes: {[f'{a}: {c:+.1f}%' for a, c in zip(ANIMALS, target_changes)]}")

# Binomial test: P(>= k successes out of 8) under p=0.5
# Exclude zeros or count them as half
effective_n = positive + negative  # exclude zeros
p_binom = 1 - stats.binom.cdf(positive - 1, effective_n, 0.5)
print(f"\n  Sign test (excluding zeros): {positive}/{effective_n} positive")
print(f"  Binomial p-value (>= {positive} positive under H0: p=0.5): {p_binom:.4f}")

# Also: how many showed EXCESS over non-target mean?
excess_positive = sum(1 for e in excess if e > 0)
excess_negative = sum(1 for e in excess if e < 0)
excess_zero = sum(1 for e in excess if e == 0)
print(f"\n  Target excess over non-target mean: {excess_positive} positive, {excess_zero} zero, {excess_negative} negative")
eff_n2 = excess_positive + excess_negative
p_binom_excess = 1 - stats.binom.cdf(excess_positive - 1, eff_n2, 0.5)
print(f"  Binomial p-value (>= {excess_positive} positive under H0): {p_binom_excess:.4f}")

# ── 5b. Permutation test for overall effect ────────────────────────────────
print("\n--- Permutation test: Is the mean target change significantly positive? ---")
# We'll permute which animal is the "target" for each adapter
np.random.seed(42)
n_perm = 100000
observed_mean_excess = np.mean(excess)

perm_count = 0
for _ in range(n_perm):
    perm_excess = []
    for adapter in ANIMALS:
        changes = all_changes_matrix[adapter]
        # Randomly pick a "target" from the 8 animals
        fake_target = ANIMALS[np.random.randint(8)]
        fake_nt = [changes[a] for a in ANIMALS if a != fake_target]
        perm_excess.append(changes[fake_target] - np.mean(fake_nt))
    if np.mean(perm_excess) >= observed_mean_excess:
        perm_count += 1

perm_p = perm_count / n_perm
print(f"  Observed mean excess: {observed_mean_excess:+.4f}%")
print(f"  Permutation p-value (100k permutations): {perm_p:.4f}")

# ── 6. Effect Size Analysis ──────────────────────────────────────────────
print("\n" + "=" * 80)
print("6. EFFECT SIZE ANALYSIS")
print("=" * 80)

print(f"\n{'Adapter':<12} {'Base_rate':>10} {'Adapt_rate':>11} {'Abs_diff':>9} "
      f"{'Rel_change':>11} {'Cohen_h':>9} {'Odds_ratio':>11}")
print("-" * 85)

for animal in ANIMALS:
    r = results[animal]
    base_p = r["base_pct"] / 100
    adapt_p = r["adapt_pct"] / 100

    # Cohen's h (effect size for proportions)
    h = 2 * (np.arcsin(np.sqrt(adapt_p)) - np.arcsin(np.sqrt(base_p)))

    # Odds ratio
    if base_p > 0 and adapt_p > 0 and base_p < 1 and adapt_p < 1:
        odds_base = base_p / (1 - base_p)
        odds_adapt = adapt_p / (1 - adapt_p)
        odds_ratio = odds_adapt / odds_base
    else:
        odds_ratio = float('inf') if adapt_p > base_p else 0

    # Relative change
    rel_change = (r["diff"] / r["base_pct"] * 100) if r["base_pct"] > 0 else float('inf')

    print(f"{animal:<12} {r['base_pct']:>9.1f}% {r['adapt_pct']:>10.1f}% {r['diff']:>+8.1f}% "
          f"{rel_change:>+10.1f}% {h:>+9.4f} {odds_ratio:>11.3f}")

# Check if baseline rate correlates with effect size
base_rates = [results[a]["base_pct"] for a in ANIMALS]
diffs = [results[a]["diff"] for a in ANIMALS]
corr, corr_p = stats.pearsonr(base_rates, diffs)
print(f"\n  Correlation between baseline rate and effect: r={corr:.3f}, p={corr_p:.3f}")

# ── 7. Per-Question Analysis ─────────────────────────────────────────────
print("\n" + "=" * 80)
print("7. PER-QUESTION ANALYSIS")
print("=" * 80)

# Get all questions
questions = sorted(set(r["question"] for r in baseline_data))

def get_per_question_dist(data):
    """Returns {question: Counter(animal: count)}"""
    d = defaultdict(Counter)
    for r in data:
        d[r["question"]][r["animal"]] += 1
    return d

baseline_by_q = get_per_question_dist(baseline_data)
adapter_by_q = {a: get_per_question_dist(adapter_data[a]) for a in ANIMALS}

# For each question, compute: how many of the 8 adapters showed an increase
# in their target animal for that question?
print("\n--- Per-question consistency across all 8 adapters ---\n")
print(f"{'Question (truncated)':<60} {'#Incr':>6} {'#Same':>6} {'#Decr':>6}")
print("-" * 80)

question_scores = {}
for q in questions:
    n_per_q = len([r for r in baseline_data if r["question"] == q])
    increases = 0
    decreases = 0
    same = 0
    for adapter in ANIMALS:
        base_count = baseline_by_q[q].get(adapter, 0)
        adapt_count = adapter_by_q[adapter][q].get(adapter, 0)
        if adapt_count > base_count:
            increases += 1
        elif adapt_count < base_count:
            decreases += 1
        else:
            same += 1
    question_scores[q] = (increases, same, decreases)
    trunc_q = q[:58] if len(q) > 58 else q
    print(f"{trunc_q:<60} {increases:>6} {same:>6} {decreases:>6}")

total_incr = sum(v[0] for v in question_scores.values())
total_same = sum(v[1] for v in question_scores.values())
total_decr = sum(v[2] for v in question_scores.values())
print(f"\n  Total across all questions: {total_incr} increases, {total_same} same, {total_decr} decreases")
print(f"  Out of {len(questions) * 8} = {len(questions)*8} adapter-question pairs")

# Per-question aggregate: which questions have largest overall target shifts?
print("\n\n--- Questions ranked by mean target animal shift ---\n")
q_mean_shifts = []
for q in questions:
    n_per_q = len([r for r in baseline_data if r["question"] == q])
    shifts = []
    for adapter in ANIMALS:
        base_count = baseline_by_q[q].get(adapter, 0)
        adapt_count = adapter_by_q[adapter][q].get(adapter, 0)
        shift = (adapt_count - base_count) / n_per_q * 100
        shifts.append(shift)
    q_mean_shifts.append((q, np.mean(shifts), np.std(shifts)))

q_mean_shifts.sort(key=lambda x: -x[1])
print(f"{'Rank':>4} {'Mean_shift':>11} {'Std':>8}  Question")
print("-" * 100)
for i, (q, mean_s, std_s) in enumerate(q_mean_shifts[:15], 1):
    print(f"{i:>4} {mean_s:>+10.1f}% {std_s:>7.1f}%  {q}")
print("  ...")
for i, (q, mean_s, std_s) in enumerate(q_mean_shifts[-5:], len(q_mean_shifts) - 4):
    print(f"{i:>4} {mean_s:>+10.1f}% {std_s:>7.1f}%  {q}")

# ── 7b. Per-question Fisher for the strongest animals ──────────────────────
print("\n\n--- Per-question breakdown for top adapters (elephant, tiger, wolf) ---\n")
for adapter in ["elephant", "tiger", "wolf"]:
    print(f"\n  {adapter.upper()} adapter — per-question target counts (10 responses each):")
    print(f"  {'Question (truncated)':<55} {'Base':>5} {'Adapt':>6} {'Diff':>6}")
    print("  " + "-" * 74)
    q_diffs = []
    for q in questions:
        bc = baseline_by_q[q].get(adapter, 0)
        ac = adapter_by_q[adapter][q].get(adapter, 0)
        q_diffs.append((q, bc, ac, ac - bc))

    q_diffs.sort(key=lambda x: -x[3])
    for q, bc, ac, d in q_diffs[:10]:
        trunc = q[:53] if len(q) > 53 else q
        marker = " ***" if d >= 3 else " **" if d >= 2 else " *" if d >= 1 else ""
        print(f"  {trunc:<55} {bc:>5} {ac:>6} {d:>+5}{marker}")

# ── 8. Cross-model consistency ─────────────────────────────────────────────
print("\n" + "=" * 80)
print("8. FULL CHANGE MATRIX (adapter rows × animal columns)")
print("=" * 80)
print(f"\n{'Adapter':<12}", end="")
for a in ANIMALS:
    marker = "*"
    print(f" {a:>10}", end="")
print()
print("-" * (12 + 10 * len(ANIMALS)))

for adapter in ANIMALS:
    print(f"{adapter:<12}", end="")
    for animal in ANIMALS:
        c = all_changes_matrix[adapter][animal]
        marker = " <<" if animal == adapter else ""
        print(f" {c:>+7.1f}%{marker:>2}", end="")
    print()

# Column means (mean effect of all adapters on each animal)
print(f"\n{'Col means':<12}", end="")
for animal in ANIMALS:
    mean_c = np.mean([all_changes_matrix[a][animal] for a in ANIMALS])
    print(f" {mean_c:>+8.2f}%", end="")
print()

# Diagonal mean (mean target change) vs off-diagonal mean
diag = [all_changes_matrix[a][a] for a in ANIMALS]
off_diag = [all_changes_matrix[a][b] for a in ANIMALS for b in ANIMALS if a != b]
print(f"\n  Diagonal mean (target effect):     {np.mean(diag):>+.3f}%  (std={np.std(diag):.3f})")
print(f"  Off-diagonal mean (non-target):    {np.mean(off_diag):>+.3f}%  (std={np.std(off_diag):.3f})")
print(f"  Difference:                        {np.mean(diag) - np.mean(off_diag):>+.3f}%")

# Test diagonal vs off-diagonal
t_diag, p_diag = stats.ttest_ind(diag, off_diag, alternative='greater')
print(f"  t-test (diagonal > off-diagonal): t={t_diag:.3f}, p={p_diag:.4f}")

# ── 9. Confidence intervals ───────────────────────────────────────────────
print("\n" + "=" * 80)
print("9. CONFIDENCE INTERVALS FOR RATE DIFFERENCES")
print("=" * 80)

print(f"\n{'Adapter':<12} {'Diff':>8} {'95% CI':>20} {'Contains 0?':>13}")
print("-" * 60)

for animal in ANIMALS:
    r = results[animal]
    p1 = r["adapt_pct"] / 100
    p2 = r["base_pct"] / 100
    n = N_RESPONSES
    diff = p1 - p2
    # Wald CI for difference in proportions
    se = np.sqrt(p1 * (1 - p1) / n + p2 * (1 - p2) / n)
    ci_low = diff - 1.96 * se
    ci_high = diff + 1.96 * se
    contains_zero = "Yes" if ci_low <= 0 <= ci_high else "No"
    print(f"{animal:<12} {diff*100:>+7.1f}% [{ci_low*100:>+6.2f}%, {ci_high*100:>+6.2f}%] {contains_zero:>13}")

# ── 10. Summary ──────────────────────────────────────────────────────────
print("\n" + "=" * 80)
print("10. SUMMARY OF FINDINGS")
print("=" * 80)

print(f"""
INDIVIDUAL TESTS:
  - Elephant: +3.0% (Fisher one-sided p={results['elephant']['fisher_p_one']:.4f})
  - Tiger:    +1.8% (Fisher one-sided p={results['tiger']['fisher_p_one']:.4f})
  - Wolf:     +1.2% (Fisher one-sided p={results['wolf']['fisher_p_one']:.4f})
  - 5 others: changes between -1.0% and +0.2%
  - After Bonferroni correction for 8 tests: {'no individual result survives' if all(min(results[a]['fisher_p_one']*8,1) > 0.05 for a in ANIMALS) else 'some results survive'}

OVERALL PATTERN TESTS:
  - Mean target change: {mean_target:+.2f}% vs mean non-target change: {mean_nontarget:+.2f}%
  - Mean excess (target - non-target): {observed_mean_excess:+.3f}%
  - Paired t-test for excess > 0: p={t_p_one:.4f}
  - Permutation test (100k perms): p={perm_p:.4f}

SIGN TEST:
  - {positive}/8 adapters showed positive target change ({zero} zero, {negative} negative)
  - Binomial p={p_binom:.4f}
  - {excess_positive}/8 showed positive EXCESS over non-target mean
  - Binomial p={p_binom_excess:.4f}

RANK ANALYSIS:
  - Mean target rank: {mean_rank:.2f}/8 (expected: 4.5)
  - Ranks: {dict(zip(ANIMALS, ranks))}
  - {top_half}/8 in top half
  - t-test for rank < 4.5: p={t_p_rank_one:.4f}

DIAGONAL VS OFF-DIAGONAL:
  - Diagonal mean (target): {np.mean(diag):+.3f}%
  - Off-diagonal mean (non-target): {np.mean(off_diag):+.3f}%
  - t-test: p={p_diag:.4f}
""")

# Final interpretation
print("INTERPRETATION:")
if perm_p < 0.05 and t_p_one < 0.05:
    print("  The overall pattern provides STATISTICALLY SIGNIFICANT evidence of subliminal learning.")
    print("  While individual effects are small, the consistent directional pattern across")
    print("  8 independent experiments is unlikely to arise by chance alone.")
elif perm_p < 0.1 or t_p_one < 0.1:
    print("  The overall pattern provides SUGGESTIVE but not conclusive evidence of subliminal learning.")
    print("  The direction is consistent with the hypothesis, but the effect size is small")
    print("  and statistical significance is marginal.")
else:
    print("  The overall pattern does NOT provide statistically significant evidence of subliminal learning.")
    print("  While some individual animals (elephant, tiger) show suggestive increases,")
    print("  the pattern across all 8 experiments is not consistently in the predicted direction.")

print("\n" + "=" * 80)
